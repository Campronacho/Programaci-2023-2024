/* aquest programa pregunta a l’usuari un nombre de l’1 al 10 i n’imprimeixi la seva taula de multiplicar.
*/

#include <iostream>
using namespace std;

int main () {
    int number;
    cout << "Escriu un numero de l'1 al 10: ";
    cin >> number;
    cout << number << "x1 = " << number*1 << endl;
    cout << number << "x2 = " << number*2 << endl;
    cout << number << "x3 = " << number*3 << endl;
    cout << number << "x4 = " << number*4 << endl;
    cout << number << "x5 = " << number*5 << endl;
    cout << number << "x6 = " << number*6 << endl;
    cout << number << "x7 = " << number*7 << endl;
    cout << number << "x8 = " << number*8 << endl;
    cout << number << "x9 = " << number*9 << endl;
    cout << number << "x10 = " << number*10 << endl;


}
